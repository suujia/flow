package main

import (
	"database/sql"
	"fmt"
	"log"
	"net/http"
	"os"

	"github.com/gorilla/mux"
	"github.com/suujia/flow/api/spothandler"
)

var db *sql.DB

const (
	dbhost = "DBHOST"
	dbport = "DBPORT"
	dbuser = "DBUSER"
	dbpass = "DBPASS"
	dbname = "DBNAME"
)

func main() {
	initDb()
	defer db.Close()
	// Sample Data
	// spots = append(spots, Spot{ID: "1", Name: "Green Leaf Sushi", Location: "on broadway", Tags: []string{"cute", "fun"}})
	// spots = append(spots, Spot{ID: "2", Name: "Little Sheep Hot Pot", Location: "also on broadway", Tags: []string{"yummy", "free parking"}})
	router := mux.NewRouter()
	router.HandleFunc("/flow", spothandler.GetSpots).Methods("GET")
	router.HandleFunc("/flow/{id}", spothandler.GetSpot).Methods("GET")
	router.HandleFunc("/flow/spot/{id}", spothandler.CreateRating).Methods("POST")
	log.Fatal(http.ListenAndServe(":8080", router))
}

// Initializes the database
func initDb() {
	config := dbConfig()
	var err error
	psqlInfo := fmt.Sprintf("host=%s port=%s user=%s "+
		"password=%s dbname=%s sslmode=disable",
		config[dbhost], config[dbport],
		config[dbuser], config[dbpass], config[dbname])

	db, err = sql.Open("postgres", psqlInfo)
	if err != nil {
		panic(err)
	}
	err = db.Ping()
	if err != nil {
		panic(err)
	}
	fmt.Println("Successfully connected!")
}

// Check db configurations are set
func dbConfig() map[string]string {
	conf := make(map[string]string)
	host, ok := os.LookupEnv(dbhost)
	if !ok {
		panic("DBHOST environment variable required but not set")
	}
	port, ok := os.LookupEnv(dbport)
	if !ok {
		panic("DBPORT environment variable required but not set")
	}
	user, ok := os.LookupEnv(dbuser)
	if !ok {
		panic("DBUSER environment variable required but not set")
	}
	password, ok := os.LookupEnv(dbpass)
	if !ok {
		panic("DBPASS environment variable required but not set")
	}
	name, ok := os.LookupEnv(dbname)
	if !ok {
		panic("DBNAME environment variable required but not set")
	}
	conf[dbhost] = host
	conf[dbport] = port
	conf[dbuser] = user
	conf[dbpass] = password
	conf[dbname] = name
	return conf
}
